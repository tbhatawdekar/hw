public class Main {
  public static void main(String[] args) {
    //1
    //12
    //123
    //1234
    //12345
    
   
    
    // int, float, string
    
    for(int i=1; i<=5; i++){
    	//i --> 1, 2, 3, 4, 5
		
        
        for (int j=1; j<=10; j++){
        	// j --> 1, 2, 3, 4 ..., 9, 10
            
            // 1 x 1 = 1
            // 1 x 2 = 2
            // i x j = i*j 
        	System.out.println(i + " x " + j + " = " + i*j);
        }
        
        System.out.print("\n");
        
        
    }
  }
}
